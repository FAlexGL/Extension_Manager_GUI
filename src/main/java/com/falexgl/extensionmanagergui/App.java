    private void jButtonAceptarSustituirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarSustituirActionPerformed
        if(jTextFieldNuevaExtensionSustituir.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Al no indicar una nueva extensión, se eliminará la extensión oritinal dejando a los archivos sin extensión.");
        }
        String texto = "<html><p><strong>Configuración seleccionada:</strong></p>"
                + "<ul><li>Extensión original: <strong>." + jComboBox1.getSelectedItem().toString() + "</strong></li>";
        texto += (jTextFieldNuevaExtensionSustituir.getText().equals(""))
                ? "<li><strong>Se eliminará la extensión de los archivos con la extensión original indicada.</strong></li>"
                : "<li>Nueva extensión: <strong>." + jTextFieldNuevaExtensionSustituir.getText() + "</strong></li>";
        texto += (jRadioButtonConservar.isSelected())
                ? "<li>Se <strong>conservarán</strong> los archivos originales.</li></ul>"
                : "<li>Se <strong>eliminarán</strong> los archivos originales.</li></ul>";
        texto += "<p>¿Desea continuar?</p></html>";

        esSustituir = true;

        jLabelConfirmarSustituir.setText(texto);
        jDialogConfirmarSustituir.setSize(330, 210);
        jDialogConfirmarSustituir.setLocationRelativeTo(this);
        jDialogConfirmarSustituir.setVisible(true);
    }//GEN-LAST:event_jButtonAceptarSustituirActionPerformed

