    private void jButtonOkMainActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOkMainActionPerformed
        if (jRadioButtonListar.isSelected()) {
            jLabelContenidoCarpeta.setText(em.mostrarContenido());
            jDialogMostrarContenido.setSize(500, 350);
            jDialogMostrarContenido.setLocationRelativeTo(this);
            jDialogMostrarContenido.show();
            jDialogMostrarContenido.setVisible(true);
        } else if (jRadioButtonSustituir.isSelected()) {
            jComboBox1.removeAllItems();
            HashMap<String, ArrayList<String>> agrupacionExtensiones = em.getAgrupacionExtensiones();
            for (Map.Entry<String, ArrayList<String>> entry : agrupacionExtensiones.entrySet()) {
                if (!entry.getKey().equals("")) {
                    jComboBox1.addItem(entry.getKey());
                }
            }
            jDialogSustituir.setSize(400, 250);
            jDialogSustituir.setLocationRelativeTo(this);
            jDialogSustituir.setVisible(true);
        } else if (jRadioButtonAnadir.isSelected()) {
            HashMap<String, ArrayList<String>> agrupacionExtensiones = em.getAgrupacionExtensiones();
            if (!agrupacionExtensiones.containsKey("")) {
                JOptionPane.showMessageDialog(rootPane, "No hay archivos sin extensión en la ubicación actual.");
            } else {
                jLabelAnadirExtension.setText("<html>Se va a añadir extensión a <strong>" + agrupacionExtensiones.get("").size() + " archivo/s</strong>.</html>");
                jDialogAnadirExtension.setSize(440, 225);
                jDialogAnadirExtension.setLocationRelativeTo(this);
                jDialogAnadirExtension.setVisible(true);
            } 

        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecciona alguna opción para poder continuar.");
        }

    }//GEN-LAST:event_jButtonOkMainActionPerformed

