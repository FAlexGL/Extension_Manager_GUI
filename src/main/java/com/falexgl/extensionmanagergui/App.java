    private void jButtonAceptarAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarAnadirActionPerformed
        if (jTextFieldAnadirExtension.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe indicar la extensión que quiera añadir.");
        } else {
            String texto = "<html><p><strong>Configuración seleccionada:</strong></p>"
                    + "<ul><li>Se añadirá la siguiente extensión: .<strong>" + jTextFieldAnadirExtension.getText() + "</strong></li>";
            texto += (jRadioButtonConservar1.isSelected())
                    ? "<li>Se <strong>conservarán</strong> los archivos originales.</li></ul>"
                    : "<li>Se <strong>eliminarán</strong> los archivos originales.</li></ul>";
            texto += "<p>¿Desea continuar?</p></html>";

            esSustituir = false;
            
            jLabelConfirmarSustituir.setText(texto);
            jDialogConfirmarSustituir.setSize(330, 180);
            jDialogConfirmarSustituir.setLocationRelativeTo(this);
            jDialogConfirmarSustituir.setVisible(true);
        }
    }//GEN-LAST:event_jButtonAceptarAnadirActionPerformed

